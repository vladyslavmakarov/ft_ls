#include "libft.h"

int	ft_3dlen(char ***array)
{
	int i;
	int j;
	int k;

	i = 0;
	while (array[i] != '\0')
	{
		j = 0;
		while (array[i][j] != '\0')
		{
			k = 0;
			while (array[i][j][k] != '\0')
				k++;
			j++;
		}
		i++;
	}
	return (i);
}

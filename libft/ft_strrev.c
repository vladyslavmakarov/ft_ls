#include "libft.h"

char	*ft_strrev(char *str)
{
	int		i;
	int		len;
	char	temp;

	if (str == NULL)
		return (NULL);
	i = 0;
	len = ft_strlen(str) - 1;
	while (i < len)
	{
		temp = str[i];
		str[i] = str[len];
		str[len] = temp;
		i++;
		len--;
	}
	return (str);
}

#include "libft.h"

int	ft_2dlen(char **array)
{
	int i;
	int j;

	i = 0;
	while (array[i] != '\0')
	{
		j = 0;
		while (array[i][j] != '\0')
			j++;
		i++;
	}
	return (i);
}
